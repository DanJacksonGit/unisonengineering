        <h3 class="blog-post-title">
          Validation Information
        </h3>
        <p><strong>
          The European Commission's Expert Group for Technical Advice on Organic Production (EGTOP) adopted the following technical advice at the 12th plenary meeting of 14 – 15 December 2015 and submitted the final version on 11 January 2016. <a href="https://ec.europa.eu/info/sites/info/files/food-farming-fisheries/farming/documents/final_report_egtop_on_cleaning_disinfectant_en.pdf">EGTOP/2016</a></strong> 
        </p>
        <p>
          The electrolysed water technology (Unison ECA) is used to generate solutions of hypochlorous acid, which are then applied as disinfectant. In the Group’s opinion, this should be regulated identically to the use of sodium hypochlorite, because in both cases there is a pH-dependent equilibrium of hypochlorite and hypochlorous acid.
        </p>
        <p>
          In the Group’s opinion, the current listing of sodium hypochlorite in <a href="../../brochures/Annex7.pdf">Annex VII</a> covers in-situ generation as well as industrial production. However, it may be that the generation in situ creates less concern regarding production of by-products <cite><a href="https://ec.europa.eu/info/sites/info/files/food-farming-fisheries/farming/documents/final_report_egtop_on_cleaning_disinfectant_en.pdf">
              (Fong et al, cited in EGTOP, 2016 p.44)
            </a></cite>
        </p>
        <h3>
          Conclusions
        </h3>
        <p>
          In the Group’s opinion, the use of electrolysed water is similar to the use of sodium hypochlorite. It may therefore be used for all purposes for which sodium hypochlorite is authorised, but not for any other purposes.
        </p>
        <p>
          For the time being the Group sees no need to mention electrolysed water explicitly. However, if there should be more data showing that this technology leads to significantly lower levels of chlorinated by-products, a difference could be made between use of hypochlorites as such and the use of electrolysed water in the future.
        </p>
        <p>
          In accordance with Article 89 of <a href="https://eur-lex.europa.eu/legal-content/EN/TXT/?uri=CELEX:32012R0528">Regulation (EU) No 528/2012</a> of the European Parliament and of the Council 22 May 2012 concerning the making available on the market and use of biocidal products (BPR), the Biocidal Products Committee (BPC) has adopted this opinion on the approval in product type 5 of the following active substance:
        </p>
        <p>
          <strong>Common name:</strong> Active chlorine generated from sodium chloride by electrolysis.
        </p>
        <p>
          Reactors are listed in <!-- Article 95 of BPR 528/2012 -->ECHA/BPC/198/2018 <!-- of the  -->(The European Chemical Agency), with the entry: <a href="https://echa.europa.eu/documents/10162/a35c8880-168e-a6a5-2be0-5699f50e4ca9">Active chlorine generated from sodium chloride by electrolysis (ECHA/BPC/198/2018).</a><!-- (Pages 9/110 - 25/110 and 28/110 - 32/110) -->
        </p>
        <p>
          This regulation is valid in every EU member state. We use these reactors.
